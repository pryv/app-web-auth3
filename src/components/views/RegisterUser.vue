<template>
  <div>
    <h1>{{ $t('register') }}</h1>

    <v-form
      v-if="newUser==null"
      id="registerForm"
      ref="form"
      v-model="validForm"
      @submit.prevent>

      <v-text-field
        id="email"
        v-model="email"
        :rules="[rules.required, rules.email]"
        :label="$t('email')"
      />

      <v-text-field
        id="username"
        v-model="ctx.user.username"
        :rules="[rules.required]"
        :label="$t('username')"
      />

      <Password
        v-model="password"
        :confirmation="true"/>

      <v-autocomplete
        id="hosting"
        v-model="hosting"
        :items="hostingsSelection"
        :rules="[rules.required]"
        :label="$t('hosting')"
        :placeholder="$t('chooseHosting')"

      />

      <v-btn
        id="submitButton"
        :disabled="!validForm||submitting"
        @click="submit"
      >{{ $t('createUser') }}</v-btn>

      <v-btn
        id="clearButton"
        @click="clear"
      >{{ $t('clear') }}</v-btn>

      <div>
        {{ $t('terms1') }}
        <a
          target="_blank"
          href="https://pryv.com/terms-of-use/">
        {{ $t('terms2') }}</a>.
      </div>
    </v-form>

    <div v-if="ctx.permissions.list != null">
      <v-divider class="mt-3 mb-2"/>
      <router-link :to="{ name: 'Authorization' }"><h3>{{ $t('backToSignin') }}</h3></router-link>
    </div>

    <Alerts
      :successMsg="success"
      :errorMsg="error"/>
  </div>
</template>

<script>
import Password from './bits/Password.vue';
import Alerts from './bits/Alerts.vue';
import Context from '../../context.js';
import controllerFactory from '../controller/controller.js';
import { i18n } from '../../locals/i18n.js';

export default {
  components: {
    Password,
    Alerts,
  },
  data: () => ({
    password: '',
    email: '',
    hosting: '',
    hostingsSelection: [],
    newUser: null,
    submitting: false,
    ctx: {},
    c: null,
    error: '',
    success: '',
    rules: {
      required: value => !!value || i18n.t('requireField'),
      email: value => /.+@.+/.test(value) || i18n.t('invalidEmail'),
    },
    validForm: false,
  }),
  created () {
    this.ctx = new Context(this.$route.query);
    this.c = controllerFactory(this.ctx);
    // Fill selector with available hostings, preselect first one
    this.c.loadHostings()
      .then(this.initHostings)
      .catch(this.showError);
  },
  methods: {
    submit () {
      if (this.$refs.form.validate()) {
        this.submitting = true;
        this.c.createUser(this.password, this.email, this.hosting)
          .then((newUser) => {
            this.newUser = newUser;
            // If the goal was only to register a new user (no requested permissions)
            // then we just redirect the new user to its pryv core
            if (this.ctx.permissions.list == null) {
              location.href = this.ctx.pryv.core(newUser.username);
            }
            this.success = `${i18n.t('userCreated')} ${newUser.username}.`;
          })
          .catch(this.showError)
          .finally(() => { this.submitting = false; });
      }
    },
    clear () {
      this.$refs.form.reset();
    },
    initHostings (hostings) {
      this.hostingsSelection = hostings;
      if (this.hostingsSelection.length > 0) {
        this.hosting = this.hostingsSelection[0].value;
      }
    },
    showError (error) {
      this.error = error.msg;
    },
  },
};
</script>
